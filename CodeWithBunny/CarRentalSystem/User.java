package CodeWithBunny.CarRentalSystem;

public class User {
    private final String name;
    private final String age;
    private final String email;
    private final String pan;
    private final String liscence;

    public User(String name, String age, String email, String pan, String liscence){
        this.name = name;
        this.age = age;
        this.email = email;
        this.pan = pan;
        this.liscence = liscence;
    }
}
